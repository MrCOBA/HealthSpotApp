                           compatibleWith: nil) ?? UIColor()
        }

        static var backgroundPrimaryColor: UIColor {
            return UIColor(named: "BackgroundPrimary_Cappuccino",
                           in: .uikit,
                           compatibleWith: nil) ?? UIColor()
        }

        static var backgroundSecondaryColor: UIColor {
            return UIColor(named: "BackgroundSecondary_Cappuccino",
                           in: .uikit,
                           compatibleWith: nil) ?? UIColor()
        }

        static var textColor: UIColor {
            return UIColor(named: "Text_Cappuccino",
                           in: .uikit,
                           compatibleWith: nil) ?? UIColor()
        }

    }

}
